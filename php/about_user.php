<h1>Game Rules</h1>

<p style="margin-left: 230px; margin-right: 230px;  font-size: 130% "><br>
Surakarta is a multi-player reaction based game. Player 
can win the game by eliminating all the chess of your opponent on the 
game board.

The player can move the chess of his own colour by one step each turn.
</p> 



<p style="margin-left: 230px; margin-right: 230px;  font-size: 130% "><br>
Select the chess you want to move by double clicking that chess, move you mouse 
to change the position of the chess,finally place the chess in thr correct position by click
the game board. You score will be increased by 20 in you capture you opponent's chess each time. 
</p> 

<p style="margin-left: 230px; margin-right: 230px;  font-size: 130% "><br>
